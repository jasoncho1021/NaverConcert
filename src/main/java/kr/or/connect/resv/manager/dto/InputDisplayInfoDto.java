package kr.or.connect.resv.manager.dto;

public class InputDisplayInfoDto {
	String openingHours;
	String placeName;
	String placeLot;
	String placeStreet;
	String tel;
	String homepage;
	String email;

	public String getOpeningHours() {
		return openingHours;
	}

	public void setOpeningHours(String openingHours) {
		this.openingHours = openingHours;
	}

	public String getPlaceName() {
		return placeName;
	}

	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}

	public String getPlaceLot() {
		return placeLot;
	}

	public void setPlaceLot(String placeLot) {
		this.placeLot = placeLot;
	}

	public String getPlaceStreet() {
		return placeStreet;
	}

	public void setPlaceStreet(String placeStreet) {
		this.placeStreet = placeStreet;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getHomepage() {
		return homepage;
	}

	public void setHomepage(String homepage) {
		this.homepage = homepage;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	@Override
	public String toString() {
		return "InputDisplayInfoDto [openingHours=" + openingHours + ", placeName=" + placeName + ", placeLot="
				+ placeLot + ", placeStreet=" + placeStreet + ", tel=" + tel + ", homepage=" + homepage + ", email="
				+ email + "]";
	}

}
